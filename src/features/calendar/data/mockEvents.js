import dayjs from "dayjs";

const createMockEvent = ({
  baseDay,
  id,
  title,
  hour,
  minute,
  durationMinutes,
  avatar,
  location,
  isRegistered,
  color,
}) => {
  const startTime = baseDay.hour(hour).minute(minute).second(0).millisecond(0);
  const endTime = startTime.add(durationMinutes, "minute");

  return {
    id,
    eventId: id,
    title,
    startTime: startTime.toISOString(),
    endTime: endTime.toISOString(),
    thumbnailUrl: avatar,
    location,
    isRegistered,
    color,
  };
};

